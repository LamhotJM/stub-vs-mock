package edu.mum.cs.cs425.lesson15.tddwithjunit;

public class ArithmeticOps {
	
	/**
	 * Adds the given two integers, a and b, 
	 * and returns the sum.
	 * @param a integer input
	 * @param b integer input
	 * @return the sum of a and b.
	 */
	public int add(int a, int b) {
		return a + b;
	}
	
	/**
	 * Subtracts the integer, b from the integer a, 
	 * and returns the difference.
	 * @param a
	 * @param b
	 * @return the difference resulting from subtracting b from a.
	 */
	public int subtract(int a, int b) {
		return a - b;
	}
}
